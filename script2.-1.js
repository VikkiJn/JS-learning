// Сложение различных типов

let num1  = 2;
let num2 = 3;
console.log( num1 + num2 ); //5

//Совмещенный тип
let num3  = 4;
console.log( num3 += 2 ); //6

//increment
let x  = 4;
console.log( x = ++x ); //5


//sum-string
console.log( "" + 1 + 0 ); //"10"

console.log( true + false ); // 1

console.log( " -2 " + 4 ); // " -2 4 "

console.log( null  + 1 ); // 1

console.log( undefined  + 1 ); // NaN


