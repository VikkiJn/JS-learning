// subtraction of different types

let temp1  = 10;
console.log( temp1 -= 2 ); // 8

console.log( "9" - "3" ); // 6

console.log( true - false ); // 1-0 = 1

console.log( null - 2 ); // 0-2 = -2

console.log( null - "2" ); // 0-2 = -2

console.log( undefined - 2 ); // NaN

console.log( undefined - "2" ); //NaN

console.log( " \t \n" - 2 ); // 0 - 2 = -2




